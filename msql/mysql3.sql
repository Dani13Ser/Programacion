create database if not exists baloncesto;
create table if not exists Equipo( idEquipo integer, 
nombre varchar(20), localidad varchar(50), pais varchar(20),
nombreCorto varchar(10), primary key(idEquipo));
create table if not exists Jugador( idJugador integer, 
idEquipo integer, nombre varchar(20), apellidos varchar(50),
alturaCM integer, edad integer, nacionalidad varchar(15),
primary key(idJugador), foreign key(idEquipo) references
Equipo(idEquipo));
create table if not exists Partido(idPartido integer, 
idEquipoLocal integer, idEquipoVisitante integer, 
puntosLocal integer, puntosVisitante integer, 
primary key(idPartido), foreign key(idEquipoLocal) references
Equipo(idEquipo), foreign key(idEquipoVisitante) references
Equipo(idEquipo));
create table if not exists datosJugadorPartido( idP integer,
idJ integer, valoracion integer, puntos integer, 
asistencias integer, rebotes integer, tapones integer, 
titular boolean, primary key(idP, idJ), foreign key(idP) 
references Partido(idPartido), foreign key (idJ) references
Jugador(idJugador));
